        <strong>What is Apply?</strong><br /><br />

        Apply is a secure online application system that allows you to apply for full-time undergraduate courses, and some part-time courses for nursing and midwifery, at universities and colleges in the United Kingdom. It is made up of different sections that you need to complete. Once your application is complete, it must be sent to us so that we can process it and send it to your chosen universities and colleges.<br /><br />

        Your application does not have to be completed all at once. The system allows you to leave a section partially completed so you can return to it later and add to or edit any information you have entered. <br /><br />

        Help is available throughout Apply to make it easier for you to provide the correct details for each section. It is important that you read the help screens <strong>before</strong> completing your application because giving the wrong or incomplete information can affect the decisions made by the universities and colleges.<br /><br />

        You can choose to display Apply in Welsh. You can amend the language before registering or once you have logged in to Apply, and if you change your mind, you can switch between English and Welsh at any time while logged in.<br /><br />






