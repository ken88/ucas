        <strong>Help is always at hand</strong><br /><br />

        We provide different types of advice to help you to complete your application.<br /><br />

        <strong>Help available in Apply</strong><br /><br />

        There are two types of help available in Apply. We provide help that gives you an overview about the current page that you are viewing, and help that is specific to any field that requires you to enter details or select an option on the screen.<br /><br />

        The overview provides information relevant to the entire section and helpful advice about filling in your details. Click on the <strong>Help</strong> link in the section navigation list on the left-hand side of the screen to view this.<br /><br />

        To access guidance that is specific to a question, click on the question mark next to the field.<br /><br />

        When you select either type of help, the information will appear in a pop-up window. You should close this window when you have finished reading the information provided.<br /><br />

        <strong>Help available from other sources</strong><br /><br />

        <strong>Our website</strong><br />
        You can find useful information and advice on our website. We explain how to register and complete each section of your application, and how to write a personal statement.<br /><br />

        <strong>Contact our Customer Experience Centre</strong><br />
        Click on the <strong>Contact us</strong> link at the top of each page to find our contact details.






